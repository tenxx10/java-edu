package chapter04;

public class OpEx2 {
	public static void main(String[] args) {
		
		int a = 5+5;
		System.out.println("5+5= "+a);
		int b = a- 5;
		System.out.println("10-5= "+b);
		int c = b*2;
		System.out.println("5*2= "+c);
		int d = c/ 5;
		System.out.println("10/5="+d);
		
		// 나머지 연산
		int e = 10 % 3; //10나누기 3의 결과몫은 3, 나머지는 1이다
		System.out.println("10% 3= "+e);
	}

}
